package com.mihai.whatsappclone.message;

/**
 * Enum representing the various types of messages that can be sent in a chat.
 */
public enum MessageType {

    TEXT,  // Represents a plain text message.
    IMAGE, // Represents a message containing an image.
    AUDIO, // Represents a message containing an audio file.
    VIDEO  // Represents a message containing a video file.
}
